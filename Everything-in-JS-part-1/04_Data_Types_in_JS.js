// JavaScript is a dynamically typed (also called loosely typed) scripting language. 

// In JavaScript, variables can receive different data types over time. 

// The latest ECMAScript standard defines eight data types Out of which seven data types are Primitive (predefined) and one complex or Non-Primitive.



// ***********Primitive Data Types**************

// The predefined data types provided by JavaScript language are known as primitive data types. 
// Primitive data types are also known as in-built data types.

// 01. Number: 
let num = 69 //JavaScript numbers are always stored in double-precision 64-bit binary format IEEE 754. Unlike other programming languages, you don’t need int, float, etc to declare different numeric values.


// 02. String: 
let name = "Raj" //JavaScript Strings are similar to sentences. They are made up of a list of characters, which is essentially just an “array of characters, like “Hello GeeksforGeeks” etc.


// 03. Boolean: 
let statement = true //Represent a logical entity and can have two values: true or false.


// 04. Null: 
let x = null //This type has only one value that is null.


// 05.Undefined: 
let y; //A variable that has not been assigned a value is undefined.


// 06. Symbol: 
//Symbols return unique identifiers that can be used to add unique property keys to an object that won’t collide with keys of any other code that might add to the object.


// 07. BigInt: 
let z = 6446n //BigInt is a built-in object in JavaScript that provides a way to represent whole numbers larger than 253-1.




