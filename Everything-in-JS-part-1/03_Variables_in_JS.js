// Variables are Containers for Storing Data
// JavaScript Variables can be declared in 4 ways:

// Automatically
name = "Raj"

// Using var
value = 69 //It has a function-scoped or globally-scoped behavior.

// Using let
let x = 64  //The let keyword is a block-scoped variables. It’s commonly used for variables that may change their value.

// Using const
const pi = 3.14  //The const keyword declares variables that cannot be reassigned. It’s block-scoped as well.



// Note 
/*
The var keyword was used in all JavaScript code from 1995 to 2015.

The let and const keywords were added to JavaScript in 2015.

The var keyword should only be used in code written for older browsers. */